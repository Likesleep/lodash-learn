// ─────────────────────────────────────────────────────────────────────────────
// 描述：返回一个按照指定函数去重的新数组
// ─────────────────────────────────────────────────────────────────────────────


// ─────────────────────────────────────────────────────────────────────────────
// * @param array The array to inspect.
// * @param [iteratee=_.identity] The iteratee invoked per element.

// <T>(array, iteratee): T[]

// array:
// List<T> | null | undefined
// iteratee:
// ValueIteratee<T>
// ─────────────────────────────────────────────────────────────────────────────
import { uniqBy } from "lodash-es";



// 原始集合
const arr = [{ 'x': 1 }, { 'x': 2 }, { 'x': 1 }, { 'x': 2 }, { 'x': 1 }];
const arr1 = [2.1, 1.2, 2.3];

// ─────────────────────────────────────────────────────────────────────────────
// 1. 对对象属性进行去重
// ─────────────────────────────────────────────────────────────────────────────
console.log(
    uniqBy(arr, 'x')
);


// ─────────────────────────────────────────────────────────────────────────────
// 2. 按照指定函数去重
// ─────────────────────────────────────────────────────────────────────────────
function uniqByMathfloor(x: number) {
    return Math.floor(x);
}

console.log(
    uniqBy(arr1, uniqByMathfloor)
);