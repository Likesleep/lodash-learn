// ─────────────────────────────────────────────────────────────────────────────
// 描述：返回一个包含了所有输入数组之间共同存在的元素的新数组 (交集)
// ─────────────────────────────────────────────────────────────────────────────


// ─────────────────────────────────────────────────────────────────────────────
// * @param arrays The arrays to inspect.

// <T>(...arrays): T[]

// ...arrays: 
// Array<List<T> | null | undefined>
// ─────────────────────────────────────────────────────────────────────────────
import { intersection } from "lodash-es";



// 原始集合
const arr1 = [1, 2, 3];
const arr2 = [2, 3, 4];
const arr3 = [3, 4, 5];

// ─────────────────────────────────────────────────────────────────────────────
// 对数组查找交集
// ─────────────────────────────────────────────────────────────────────────────
console.log(
    intersection(arr1, arr2, arr3)
);
