// ─────────────────────────────────────────────────────────────────────────────
// 描述：将键值对数组转换为对象
// ─────────────────────────────────────────────────────────────────────────────


// ─────────────────────────────────────────────────────────────────────────────
// * @param pairs The key-value pairs.

// <T>(pairs: List<[PropertyName, T]> | null | undefined): Dictionary<T>

// pairs:
// List<[PropertyName, T]> | null | undefined
// ─────────────────────────────────────────────────────────────────────────────
import { fromPairs } from "lodash-es";



// 原始集合
const arr = [["a", undefined], ["b", 111], ["c", "ccc"], ["d", [1, 2, 3]]];

// ─────────────────────────────────────────────────────────────────────────────
// 键值对数组转换为对象
// ─────────────────────────────────────────────────────────────────────────────
console.log(
    "%o", fromPairs(arr),
);
// { a: undefined, b: 111, c: 'ccc', d: [ 1, 2, 3 ] }