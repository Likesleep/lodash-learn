// ─────────────────────────────────────────────────────────────────────────────
// 描述：返回一个过滤掉指定值的新数组
// ─────────────────────────────────────────────────────────────────────────────


// ─────────────────────────────────────────────────────────────────────────────
// * @param array The array to filter.
// * @param values The values to exclude.

// <T>(array: List<T> | null | undefined, ...values: T[]): T[]

// array:
// List<T> | null | undefined
// ...values:
// T[]
// ─────────────────────────────────────────────────────────────────────────────
import { without } from "lodash-es";



// 原始集合
const arr = [1, 1, 1, 3, 3, 3, 2, 2, 2, 0, 4];

// ─────────────────────────────────────────────────────────────────────────────
// 过滤数组指定值
// ─────────────────────────────────────────────────────────────────────────────
console.log(
    "%o",
    {
        resArr: without(arr, 1, 2, 3),
        originArr: arr,
    }
);

// {
//     resArr: [ 0, 4 ],
//     originArr: [ 1, 1, 1, 3, 3, 3, 2, 2, 2, 0, 4 ]
//   }