// ─────────────────────────────────────────────────────────────────────────────
// 描述：返回一个从初始位置删除指定n个元素的新数组
// ─────────────────────────────────────────────────────────────────────────────


// ─────────────────────────────────────────────────────────────────────────────
// * @param array The array to query.
// * @param n The number of elements to drop.

// <T>(array, n?): T[]

// array:
// List<T> | null | undefined
// n?:
// number
// ─────────────────────────────────────────────────────────────────────────────
import { drop } from "lodash-es";



// 原始集合
const arr = [1, 1, 1, 3, 3, 3, 2, 2, 2];

// ─────────────────────────────────────────────────────────────────────────────
// 删除指定的n个元素
// ─────────────────────────────────────────────────────────────────────────────
console.log(
    drop(arr),
    drop(arr, 8),
);