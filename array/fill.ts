// ─────────────────────────────────────────────────────────────────────────────
// 描述：对数组指定位置填充指定值
// ─────────────────────────────────────────────────────────────────────────────


// ─────────────────────────────────────────────────────────────────────────────
// * @param array The array to fill.
// * @param value The value to fill array with.
// * @param start The start position.
// * @param end The end position.

// <T, U>(array: List<U> | null | undefined, value: T, start?: number, end?: number): List<T | U>

// array:
// List<U> | null | undefined
// value:
// T
// start?:
// number
// end?:
// number
// ─────────────────────────────────────────────────────────────────────────────
import { fill } from "lodash-es";



// 原始集合
const arr = [1, 1, 1, 3, 3, 3, 2, 2, 2];

// ─────────────────────────────────────────────────────────────────────────────
// 1. 填充指定值 (不指定起始和结束位置，则默认从起始位置至结束位置)
// ─────────────────────────────────────────────────────────────────────────────
// console.log(
//     fill(arr, '*'),
// );
// [
//     '*', '*', '*',
//     '*', '*', '*',
//     '*', '*', '*'
// ]

// ─────────────────────────────────────────────────────────────────────────────
// 2. 对指定位置填充值
// ─────────────────────────────────────────────────────────────────────────────
console.log(
    fill(arr, '*', 0, 1),
);
// [
//     '*', 1, 1,
//     3, 3, 3,
//     2, 2, 2
// ]