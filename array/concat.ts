// ─────────────────────────────────────────────────────────────────────────────
// 描述：将数组与其他数组或者数值连接起来
// ─────────────────────────────────────────────────────────────────────────────


// ─────────────────────────────────────────────────────────────────────────────
// * @category Array
// * @param [values] The array values to concatenate.

// <T>(...values: Array<Many<T>>): T[]

// ...values:
// Array<Many<T>>
// ─────────────────────────────────────────────────────────────────────────────
import { concat } from "lodash-es";



// 原始集合
const arr = [1, 2, 3, 4];

// ─────────────────────────────────────────────────────────────────────────────
// 1.与数组连接
// ─────────────────────────────────────────────────────────────────────────────
console.log(
    concat(arr, [5, 6], [5, [6]]),
);
// [
//     1, 2, 3,
//     4, 5, 6,
//     5, [6]
// ]

// ─────────────────────────────────────────────────────────────────────────────
// 2.与其他值连接
// ─────────────────────────────────────────────────────────────────────────────
console.log(
    concat(arr, 5, 6, null),
);
// [
//     1, 2, 3, 4,
//     5, 6, null
// ]