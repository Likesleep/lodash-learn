// ─────────────────────────────────────────────────────────────────────────────
// 描述：返回一个将多个数组合并并去重的一维数组 (并集)
// ─────────────────────────────────────────────────────────────────────────────


// ─────────────────────────────────────────────────────────────────────────────
// * @param arrays The arrays to inspect.

// <T>(...arrays): T[]

// ...arrays:
// Array<List<T> | null | undefined>
// ─────────────────────────────────────────────────────────────────────────────
import { union } from "lodash-es";



// 原始集合
const arr1 = [1, 2, [3]];
const arr2 = [2, [3], 4];
const arr3 = [[3], 4, 5];

// ─────────────────────────────────────────────────────────────────────────────
// 对合并数组进行去重
// ─────────────────────────────────────────────────────────────────────────────
console.log(
    union(arr1, arr2, arr3)
);
// 对引用值进行去重，不同数组引用值不同
// [
//     1, 2, [3],
//     [3], 4, [3],
//     5
// ]
