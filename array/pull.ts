// ─────────────────────────────────────────────────────────────────────────────
// 描述：从数组中删除所有指定值
// ─────────────────────────────────────────────────────────────────────────────


// ─────────────────────────────────────────────────────────────────────────────
// * @param array The array to modify.
// * @param values The values to remove.

// <T>(array: List<T>, ...values: T[]): List<T>

// array:
// List<T>
// ...values:
// T[]
// ─────────────────────────────────────────────────────────────────────────────
import { pull } from "lodash-es";



// 原始集合
const arr = [1, 1, 1, 3, 3, 3, 2, 2, 2];

// ─────────────────────────────────────────────────────────────────────────────
// 删除数组所有指定值
// ─────────────────────────────────────────────────────────────────────────────

console.log(
    "%o",
    {
        resArr: pull(arr, 1, 2),
        originArr: arr
    }
);

// {
//     resArr: [ 3, 3, 3 ],
//     originArr: [ 3, 3, 3 ]
//   }
