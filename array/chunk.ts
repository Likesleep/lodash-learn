// ─────────────────────────────────────────────────────────────────────────────
// 描述：返回一个将数组按照给定size进行分组的二维数组
// ─────────────────────────────────────────────────────────────────────────────


// ─────────────────────────────────────────────────────────────────────────────
// * @param array The array to process.
// * @param size The length of each chunk.

// <T>(array, size?): T[][]

// array:
// // List<T> | null | undefined
// size?:
// number
// ─────────────────────────────────────────────────────────────────────────────
import { chunk } from "lodash-es";



// 原始集合
const arr = [1, 1, 1, 3, 3, 3, 2, 2, 2];
const arr1 = [1, 1, 1, 3, 3, 3, 2];

// ─────────────────────────────────────────────────────────────────────────────
// 对数组进行分块
// ─────────────────────────────────────────────────────────────────────────────
console.log(
    chunk(arr, 3),
    chunk(arr1, 3),
);