const str2 = "___Foo_Bar___";

// ─────────────────────────────────────────────────────────────────────────────
// 1.字符串形式
// ─────────────────────────────────────────────────────────────────────────────
console.log(
    camelCase(str),
    camelCase(str1),
    camelCase(str2),
);

