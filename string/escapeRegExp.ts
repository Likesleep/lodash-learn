// ─────────────────────────────────────────────────────────────────────────────
// 描述：转义字符串中的正则表达式特殊字符
// ─────────────────────────────────────────────────────────────────────────────


// ─────────────────────────────────────────────────────────────────────────────
// * Escapes the RegExp special characters "^", "$", "\", ".", "*", "+", "?", "(", ")", "[", "]",
// * "{", "}", and "|" in string.
// * @param string The string to escape.

// (string?: string): string;

// string?:
// string
// ─────────────────────────────────────────────────────────────────────────────
import { escapeRegExp } from "lodash-es";



// 原始字符串
const str = "^Foo$Bar\\";
const str1 = "Foo-Bar.*+?()[]";

// ─────────────────────────────────────────────────────────────────────────────
// 字符串转义
// ─────────────────────────────────────────────────────────────────────────────
console.log(
    escapeRegExp(str),
    escapeRegExp(str1),
);
// \^Foo\$Bar\\
// Foo-Bar\.\*\+\?\(\)\[\]