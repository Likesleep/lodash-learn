// ─────────────────────────────────────────────────────────────────────────────
// 描述：将给定的字符串转换为小写短横线连接的形式
// ─────────────────────────────────────────────────────────────────────────────


// ─────────────────────────────────────────────────────────────────────────────
// * @param string The string to convert.

// (string?: string): string;

// string?:
// string
// ─────────────────────────────────────────────────────────────────────────────
import { kebabCase } from "lodash-es";



// 原始字符串
const str = "Foo Bar";
const str1 = "----Foo-Bar----";
const str2 = "___Foo_Bar___";

// ─────────────────────────────────────────────────────────────────────────────
// 字符串转换
// ─────────────────────────────────────────────────────────────────────────────
console.log(
    kebabCase(str),
    kebabCase(str1),
    kebabCase(str2),
);

// foo-bar
// foo-bar
// foo-bar