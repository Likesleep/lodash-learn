// ─────────────────────────────────────────────────────────────────────────────
// 描述：移除字符串末尾的指定字符
// ─────────────────────────────────────────────────────────────────────────────


// ─────────────────────────────────────────────────────────────────────────────
// * @param string The string to trim.
// * @param chars The characters to trim.

// (string?: string, chars?: string): string

// string?:
// string
// chars?:
// string
// ─────────────────────────────────────────────────────────────────────────────
import { trimEnd } from "lodash-es";



// 原始字符串
const str = "Foo Bar   ";
const str1 = "Foo-Bar----";
const str2 = "Foo_Bar___";

// ─────────────────────────────────────────────────────────────────────────────
// 1.移除字符串末尾空白字符
// ─────────────────────────────────────────────────────────────────────────────
console.log(
    trimEnd(str),
);
// Foo Bar

// ─────────────────────────────────────────────────────────────────────────────
// 2.移除字符串末尾指定字符
// ─────────────────────────────────────────────────────────────────────────────
console.log(
    trimEnd(str1, "-"),
    trimEnd(str2, "_"),
);

// Foo-Bar
// Foo_Bar
