// ─────────────────────────────────────────────────────────────────────────────
// 描述：以居中的方式将字符串填充为指定长度
// ─────────────────────────────────────────────────────────────────────────────


// ─────────────────────────────────────────────────────────────────────────────
// * @param string The string to pad.
// * @param length The padding length.
// * @param chars The string used as padding.

// (string?: string, length?: number, chars?: string): string

// string?: string
// length?: number
// chars?: string
// ─────────────────────────────────────────────────────────────────────────────
import { pad } from "lodash-es";



// 原始字符串
const str = "Foo Bar";

// ─────────────────────────────────────────────────────────────────────────────
// 1.填充指定长度 (默认填充字符为空格)
// ─────────────────────────────────────────────────────────────────────────────
console.log(
    pad(str, 10),
);
// Foo Bar  

// ─────────────────────────────────────────────────────────────────────────────
// 2.填充指定字符串
// ─────────────────────────────────────────────────────────────────────────────
console.log(
    pad(str, 10, "*"),
);

// *Foo Bar**