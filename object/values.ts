// ─────────────────────────────────────────────────────────────────────────────
// 描述：创建一个数组，该数组值为目标对象的可枚举属性值
// ─────────────────────────────────────────────────────────────────────────────


// ─────────────────────────────────────────────────────────────────────────────
// * @param object The object to query.

// <T>(object: Dictionary<T> | NumericDictionary<T> | List<T> | null | undefined): T[]

// object:
// Dictionary<T> | NumericDictionary<T> | List<T> | null | undefined
// ─────────────────────────────────────────────────────────────────────────────
import { values } from "lodash-es";


// 原始对象
const obj = {
    a: {
        c: 1
    },
    b: [1, {
        d: [1, 2, 3]
    }, 3],
}

// ─────────────────────────────────────────────────────────────────────────────
// 返回一个对象的可枚举属性数组
// ─────────────────────────────────────────────────────────────────────────────
console.log("%o",
    values(obj)
);
// [
//     { c: 1 },
//     [ 1, { d: [ 1, 2, 3 ] }, 3 ],
// ]