// ─────────────────────────────────────────────────────────────────────────────
// 描述：创建一个继承prototype的对象
// ─────────────────────────────────────────────────────────────────────────────


// ─────────────────────────────────────────────────────────────────────────────
// * @param prototype The object to inherit from.
// * @param properties The properties to assign to the object.

// <T extends object, U extends object>(prototype: T, properties?: U): T & U

// prototype:
// T
// properties?:
// U
// ─────────────────────────────────────────────────────────────────────────────
import { create } from "lodash-es";



// ─────────────────────────────────────────────────────────────────────────────
// 创建对象
// ─────────────────────────────────────────────────────────────────────────────
const obj = create({ a: { c: 1 } }, { b: 1 })

console.log("%o",
    obj
);

// { b: 1, a: { c: 1 } }