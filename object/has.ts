// ─────────────────────────────────────────────────────────────────────────────
// 描述：检查所给路径是否为检查对象的直接属性
// ─────────────────────────────────────────────────────────────────────────────


// ─────────────────────────────────────────────────────────────────────────────
// * @param object The object to query.
// * @param path The path to check.

// <T>(object: T, path: PropertyPath): boolean

// object:
// T
// path:
// PropertyPath
// ─────────────────────────────────────────────────────────────────────────────
import { has, create } from "lodash-es";



// 原始对象
const obj = {
    a: {
        c: 1
    },
    b: [1, {
        d: [1, 2, 3]
    }, 3],
}

const obj1 = create({ a: { c: 1 } })

// ─────────────────────────────────────────────────────────────────────────────
// 检查所给路径是否为对象直接属性
// ─────────────────────────────────────────────────────────────────────────────
console.log(
    has(obj, "a"),
    has(obj, "a.c"),
    has(obj1, "a"),
);

// true
// true
// false
